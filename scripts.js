const gallonsToLiters = function(gallons) {
  return gallons*3.785;
}

const litersToGallons = function(liters) {
  return liters/3.785;
}

const tspToTbs = function(tsp) {
  return tsp / 3;
}

const tbsToTsp = function(tbs) {
  return tbs * 3;
}

const cupToOunce = function(cup) {
  return cup * 8.115;
}

const ounceToCup = function(ounce) {
  return ounce/8.115;
}